package djc.day09;

public class Demo08Math { //数学工具类

    public static void main(String[] args) {
        System.out.println(Math.abs(34.5)); //取绝对值 34.5
        System.out.println(Math.abs(-24)); //取绝对值 24
        System.out.println("================");

        System.out.println(Math.ceil(33.11)); //向上取整 34.0
        System.out.println("================");

        System.out.println(Math.floor(34.99)); //向下取整 34.0
        System.out.println("================");

        System.out.println(Math.round(34.5)); //四舍五入 35
        System.out.println(Math.round(34.4)); //四舍五入 34
        System.out.println("================");

    }
}
